//crea un oggetto temporaneo esporta tutte le funzioni dentro l'oggetto
export {somma,sottrazione,divisione,moltiplicazione};

function somma(a,b){
    return a + b;
}

function sottrazione(a,b){
    return a - b;
}

function divisione(a,b) {
    return a / b;
}

function moltiplicazione(a,b) {
    return a * b;
}
